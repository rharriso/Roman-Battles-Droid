package rharriso.RomeBattles;

public class Coalition {
	private String allies;
	private String commanders;
	private String strength;
	/**
	 * @return the allies
	 */
	public String getAllies() {
		return allies;
	}
	/**
	 * @param allies the allies to set
	 */
	public void setAllies(String allies) {
		this.allies = allies;
	}
	/**
	 * @return the commanders
	 */
	public String getCommanders() {
		return commanders;
	}
	/**
	 * @param commanders the commanders to set
	 */
	public void setCommanders(String commanders) {
		this.commanders = commanders;
	}
	/**
	 * @return the strength
	 */
	public String getStrength() {
		return strength;
	}
	/**
	 * @param strength the strength to set
	 */
	public void setStrength(String strength) {
		this.strength = strength;
	}
}
